# Create a string and convert into upper case 

name = "aditya"
print(name.upper())
print("--------------------------------------")

# Lets convert above code into function approach

def transform():
    name = "subhashbhai"
    print(name.upper())
transform()

print("--------------------------------------")

'''
        def         --> Keyword
        transform   --> User defined function
        print()     --> Predefined function
        upper()     --> Method in str class  
'''

